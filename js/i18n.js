let currentLang = localStorage.getItem('tmx_lang') || 'en';

function t(key) {
    return (I18N[currentLang] && I18N[currentLang][key]) || (I18N.en[key]) || key;
}

function setLang(lang) {
    currentLang = lang;
    localStorage.setItem('tmx_lang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    if (lang === 'ar') {
        document.body.style.fontFamily = "'Noto Sans Arabic', 'Inter', sans-serif";
    } else if (lang === 'zh') {
        document.body.style.fontFamily = "'Noto Sans SC', 'Noto Sans Arabic', 'Inter', sans-serif";
    } else {
        document.body.style.fontFamily = "'Inter', sans-serif";
    }
    applyTranslations();
    if (typeof window.onLangChange === 'function') {
        window.onLangChange(lang);
    }
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translated = t(key);
        if (el.tagName === 'INPUT' && el.type !== 'submit') {
            el.placeholder = translated;
        } else {
            el.textContent = translated;
        }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        el.innerHTML = t(el.getAttribute('data-i18n-html'));
    });
}

function initLang() {
    document.documentElement.lang = currentLang;
    document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
    if (currentLang === 'ar') {
        document.body.style.fontFamily = "'Noto Sans Arabic', 'Inter', sans-serif";
    } else if (currentLang === 'zh') {
        document.body.style.fontFamily = "'Noto Sans SC', 'Noto Sans Arabic', 'Inter', sans-serif";
    } else {
        document.body.style.fontFamily = "'Inter', sans-serif";
    }
}

function createLangSwitcher() {
    const langs = [
        { code: 'en', label: 'English', flag: 'https://flagcdn.com/w40/us.png' },
        { code: 'ar', label: 'العربية', flag: 'https://flagcdn.com/w40/eg.png' },
        { code: 'fr', label: 'Français', flag: 'https://flagcdn.com/w40/fr.png' },
        { code: 'it', label: 'Italiano', flag: 'https://flagcdn.com/w40/it.png' },
        { code: 'zh', label: '中文', flag: 'https://flagcdn.com/w40/cn.png' },
        { code: 'de', label: 'Deutsch', flag: 'https://flagcdn.com/w40/de.png' }
    ];
    const current = langs.find(l => l.code === currentLang) || langs[0];

    const wrapper = document.createElement('div');
    wrapper.className = 'lang-select-wrap';

    const btn = document.createElement('button');
    btn.className = 'lang-select-btn';
    btn.innerHTML = '<img src="' + current.flag + '" alt="" class="lang-flag"> ' + current.label + ' <i class="fas fa-chevron-down lang-chevron"></i>';

    const dropdown = document.createElement('div');
    dropdown.className = 'lang-dropdown';

    langs.forEach(l => {
        const item = document.createElement('div');
        item.className = 'lang-option' + (l.code === currentLang ? ' active' : '');
        item.innerHTML = '<img src="' + l.flag + '" alt="" class="lang-flag"> ' + l.label;
        item.addEventListener('click', () => {
            setLang(l.code);
            btn.innerHTML = '<img src="' + l.flag + '" alt="" class="lang-flag"> ' + l.label + ' <i class="fas fa-chevron-down lang-chevron"></i>';
            dropdown.querySelectorAll('.lang-option').forEach(o => o.classList.remove('active'));
            item.classList.add('active');
            dropdown.style.display = 'none';
        });
        dropdown.appendChild(item);
    });

    btn.addEventListener('click', (e) => {
        e.stopPropagation();
        dropdown.style.display = dropdown.style.display === 'block' ? 'none' : 'block';
    });

    document.addEventListener('click', () => {
        dropdown.style.display = 'none';
    });

    wrapper.appendChild(btn);
    wrapper.appendChild(dropdown);
    return wrapper;
}

